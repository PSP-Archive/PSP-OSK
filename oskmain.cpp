#include <unistd.h>
#include <stdio.h>
#include "osk.h"


//-----------------------------------------------------------------------------
// Implementations
//-----------------------------------------------------------------------------
int main(int argc_, char * argv_[])
{
  const char * cmdline = NULL;
  OskCore::OskFlags flags;
  int numVts;

  if ( argc_ >= 2 )
  {
    cmdline = argv_[ 1 ];
  }

  if ( !OskCore::ParseFlags( cmdline, flags, numVts ) )
  {
    return 0;
  }

  OskCore core( flags, numVts );
  if ( !core.Initialize( NULL, NULL ) )
  {
    return -1;
  }

  // Entering the main loop
  core.Main();

  return 0;
}


